<script setup>
import {onMounted, ref} from "vue";
import {Form, Field} from 'vee-validate';
import * as  yup from 'yup';
const users = ref({'data': []});
import { useToastr } from '../../toastr.js';
import UserListItem from "./UserListItem.vue";
const editing=ref(false);
const formValues = ref();
const form = ref(null);
const toastr = useToastr();

const getUsers = () => {
    axios.get(`/api/user`,)
        .then((response) => {
            users.value = response.data;
        })
}
const editUserSchema = yup.object({
    name: yup.string().required(),
    email: yup.string().email().required(),
    password: yup.string().when((password, schema) => {
        return password ? schema.required().min(8) : schema;
    }),
});

const createUser = (values, { resetForm, setErrors }) => {
    axios.post('/api/createUser', values)
        .then((response) => {
            users.value.unshift(response.data);
            $('#userFormModal').modal('hide');
            resetForm();
            toastr.success('User created successfully!');
        })
        .catch((error) => {
            if (error.response.data.errors) {
                setErrors(error.response.data.errors);
            }
        })
};

const addUser = () => {
    editing.value = false;
    $('#userFormModal').modal('show');
};

const editUser = (user) => {
    editing.value = true;
    form.value.resetForm();
    $('#userFormModal').modal('show');
    formValues.value = {
        id: user.id,
        name: user.name,
        email: user.email,
    };
};

const updateUser = (values) => {
    axios.put('/api/users/' + formValues.value.id, values)
        .then((response) => {
            const index = users.value.findIndex(user => user.id === response.data.id);
            users.value[index] = response.data;
            $('#userFormModal').modal('hide');
            toastr.success('User updated successfully!');
        }).catch((error) => {
        console.log(error);
    }).finally(()=>{
        form.value.resetForm();

    });
}

const handleSubmit = (values, actions) => {
    // console.log(actions);
    if (editing.value) {
        updateUser(values, actions);
    } else {
        createUser(values, actions);
    }
}

const userDeleted=(userId)=>{
    users.value= users.value.filter(user => user.id !== userId);

}





onMounted(()=>{
     getUsers();

})
</script>





<template>
    <div class="content-header">
        <div class="container-fluid">
            <div class="row mb-2">
                <div class="col-sm-6">
<!--                    <h1 class="m-0">Users Page</h1>-->
                </div>
                <div class="col-sm-6">
                    <ol class="breadcrumb float-sm-right">
                        <li class="breadcrumb-item"><a href="#">Home</a></li>
                        <li class="breadcrumb-item active">Users </li>
                    </ol>
                </div>
            </div>
        </div>
    </div>
    <div class="content">
        <div class="container-fluid">
            <div class="d-flex justify-content-between">
                <div class="d-flex">
                    <button @click="addUser" type="button" class="mb-2 btn btn-primary">
                        <i class="fa fa-plus-circle mr-1"></i>
                        Add New User
                    </button>
<!--                    <div>-->
<!--                        <button  type="button" class="ml-2 mb-2 btn btn-danger">-->
<!--                            <i class="fa fa-trash mr-1"></i>-->
<!--                            Delete Selected-->
<!--                        </button>-->
<!--                        <span class="ml-2">users</span>-->
<!--                    </div>-->
<!--                </div>-->
<!--                <div>-->
<!--                    <input type="text"  class="form-control" placeholder="Search..." />-->
<!--                </div>-->
            </div>
            </div>
            <div class="card">
                <div class="card-body">
                    <table class="table table-bordered">
                        <thead>
                        <tr>
<!--                            <th><input type="checkbox"/></th>-->
                            <th style="width: 10px">#</th>
                            <th>Name</th>
                            <th>Email</th>
                            <th>Registered Date</th>
                            <th>Role</th>
                            <th>Options</th>
                        </tr>
                        </thead>
                        <tbody>
                        <UserListItem v-for="(user,index) in users"
                                      :key="user.id"
                                      :user=user
                                      :index=index
                                      @edit-user="editUser"
                                      @user-deleted="userDeleted"
                                      />
                        </tbody>
<!--                        <tbody>-->
<!--                        <tr>-->
<!--                            <td colspan="6" class="text-center">No results found...</td>-->
<!--                        </tr>-->
<!--                        </tbody>-->
                    </table>
                </div>
            </div>
<!--            <Bootstrap4Pagination :data="users" @pagination-change-page="getUsers" />-->
        </div>
    </div>

    <div class="modal fade" id="userFormModal" data-backdrop="static"
         tabindex="-1" role="dialog" aria-labelledby="staticBackdropLabel" aria-hidden="true">
        <div class="modal-dialog" role="document">
            <div class="modal-content">
                <div class="modal-header">
                    <h5 class="modal-title" id="staticBackdropLabel">
                        <span v-if="editing">Edit User</span>
                        <span v-else>Add New User</span>
                    </h5>
                    <button type="button" class="close" data-dismiss="modal" aria-label="Close">
                        <span aria-hidden="true">&times;</span>
                    </button>
                </div>
                <Form ref="form" @submit="handleSubmit" :validation-schema="editing ? editUserSchema : createUserSchema"
                      v-slot="{ errors }" :initial-values="formValues">
                    <div class="modal-body">
                        <div class="form-group">
                            <label for="name">Name</label>
                            <Field name="name" type="text" class="form-control" :class="{ 'is-invalid': errors.name }"
                                   id="name" aria-describedby="nameHelp" placeholder="Enter full name" />
                            <span class="invalid-feedback">{{ errors.name }}</span>
                        </div>

                        <div class="form-group">
                            <label for="email">Email</label>
                            <Field name="email" type="email" class="form-control "
                                   :class="{ 'is-invalid': errors.email }" id="email" aria-describedby="nameHelp"
                                   placeholder="Enter full name" />
                            <span class="invalid-feedback">{{ errors.email }}</span>
                        </div>

                        <div class="form-group">
                            <label for="email">Password</label>
                            <Field name="password" type="password" class="form-control "
                                   :class="{ 'is-invalid': errors.password }" id="password" aria-describedby="nameHelp"
                                   placeholder="Enter password" />
                            <span class="invalid-feedback">{{ errors.password }}</span>
                        </div>
                    </div>
                    <div class="modal-footer">
                        <button type="button" class="btn btn-secondary" data-dismiss="modal">Cancel</button>
                        <button type="submit" class="btn btn-primary">Save</button>
                    </div>
                </Form>
            </div>
        </div>
    </div>



</template>
